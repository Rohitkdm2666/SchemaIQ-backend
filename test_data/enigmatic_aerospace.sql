-- Aircraft Registry Master Table
CREATE TABLE ac_registry (
    ac_reg VARCHAR(8) PRIMARY KEY,
    ac_type VARCHAR(20) NOT NULL,
    manufacturer VARCHAR(30),
    model VARCHAR(30),
    year_built INTEGER,
    owner_operator VARCHAR(50),
    max_pax INTEGER,
    max_cargo_kg INTEGER,
    mtow_kg INTEGER,
    engine_count INTEGER,
    engine_type VARCHAR(30),
    avionics_suite VARCHAR(20),
    certification VARCHAR(10),
    last_inspection DATE,
    airworthiness_cert VARCHAR(20),
    registration_country CHAR(2),
    status CHAR(1) CHECK (status IN ('A','I','R','S')), -- Active, Inactive, Retired, Scrapped
    created_ts TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Crew Licensing Table
CREATE TABLE crew_licenses (
    license_num VARCHAR(15) PRIMARY KEY,
    license_type VARCHAR(10) CHECK (license_type IN ('ATPL','CPL','PPL','CFI','ATP')),
    holder_name VARCHAR(100),
    issue_date DATE,
    expiry_date DATE,
    issuing_authority VARCHAR(50),
    aircraft_ratings TEXT,
    instrument_rating BOOLEAN DEFAULT FALSE,
    medical_class INTEGER CHECK (medical_class IN (1,2,3)),
    medical_expiry DATE,
    total_flight_hours DECIMAL(8,2),
    pic_hours DECIMAL(8,2),
    night_hours DECIMAL(6,2),
    ifr_hours DECIMAL(6,2),
    multi_engine_hours DECIMAL(6,2),
    jet_hours DECIMAL(6,2),
    status CHAR(1) CHECK (status IN ('V','S','R','E')), -- Valid, Suspended, Revoked, Expired
    created_ts TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

